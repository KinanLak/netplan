import type { Device, PortInfo } from "@/types/map";

// Helper to generate switch ports
const generatePorts = (count: number): PortInfo[] => {
    return Array.from({ length: count }, (_, i) => ({
        id: `port-${i + 1}`,
        number: i + 1,
        status: Math.random() > 0.3 ? "up" : Math.random() > 0.5 ? "down" : "unknown",
        connectedTo: Math.random() > 0.5 ? `device-${Math.floor(Math.random() * 10)}` : undefined,
    }));
};

